class Stars//nested loop
{
	public static void main (String [] args)
	{
		//outer loop in use rows
		for (int i=1;i<=4;i++)
		{
		//inner loop in use colums or datas
			
			for (int j=1;j<=3;j++)
			{
			 System.out.print("*"+" ");	
			}
			System.out.println(" ");
		}
	}
}