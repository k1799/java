class Operators
{
	public static void main(String[]args)
	{
		//Arithmetic operators
		int a=20;
		int b=60;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		//Relational(comparison) operators
		int c=10;
		int d=20;
		System.out.println(c==d);
		System.out.println(c!=d);
		System.out.println(c>d);
		System.out.println(c<d);
		System.out.println(c>=d);
		System.out.println(c<=d);
		
	}
}